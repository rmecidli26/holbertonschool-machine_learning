#!/usr/bin/env python3
"""
Bu modul iki siyahını birləşdirmək üçün funksiyanı ehtiva edir.
"""


def cat_arrays(arr1, arr2):
    """
    İki siyahını (list) birləşdirərək yeni bir siyahı qaytarır.
    Arqumentlər:
        arr1: Birinci siyahı.
        arr2: İkinci siyahı.
    Qaytarır:
        arr1 və arr2-nin elementlərindən ibarət yeni siyahı.
    """
    return arr1 + arr2
